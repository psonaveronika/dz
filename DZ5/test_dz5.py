import pytest 
from dz5 import binary_search

@pytest.mark.parametrize(
    ('x', 'y', 'z'),
    [
        ([1, 3, 5, 7, 9], 3, 1),
        ([1, 3, 5, 7, 7, 7, 9], 7, 3),
        ([0], 0, 0),
        ([-1, 0, 42], 0, 1),
        ([-42, 0, 42], 42, 2),
        ([-6, -5, -4, -3, -2, -1], -4, 2),
        ([1, 2, 3, 4, 5, 6], 4, 3),
        ([1, 2, 3, 4, 5, 6, 7], 4, 3),
        ([42, 42, 42, 42, 42], 42, 0),
        ([-42, -42, -42, -42, -42], -42, 0),
        ([42, 42, 42, 42, 43], 43, 4),
        ([41, 42, 42, 42, 42], 41, 0),
        ([-2, -2, -1, 0, 1, 2, 2, 2], -1, 2),
        ([-2, -2, -1, 0, 1, 1, 2, 2], 1, 4),

    ]
)
def test_binary_search(x,y,z):
    assert binary_search(x,y) == z


@pytest.mark.parametrize(
    ('x1', 'y1'),
    [
        ([1, 3, 5, 7, 9], -1),
        ([1, 3, 5, 7, 7, 7, 9], 8),
        ([], 42),
        ([0], 1),
        ([1, 2, 3, 4, 5], 7),
        ([1, 2, 3, 4, 5, 6], 7),
    ]
)
def test_binary_search__None(x1,y1):
    assert binary_search(x1,y1) is None